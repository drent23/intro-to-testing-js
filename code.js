// This means making sure that the sayHello function definition inside of code.js takes an an input argument. If input === "Alex", then we return "Hello, Alex!" else return "Hello, Jane!"
function sayHello(input) {
    if (input === "Alex") {
        return "Hello, Alex!";
    } else if (input === "Pat") {
        return "Hello, Pat!";
    } else {
        return "Hello, Jane!";
    }
}







